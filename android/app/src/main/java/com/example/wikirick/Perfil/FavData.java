package com.example.wikirick.Perfil;

public class FavData {


    private String name;
    private String imageUrl;

    public FavData(String name, String imageUrl) {
        this.name = name;
        this.imageUrl = imageUrl;
    }

    public String getName() {
        return name;
    }

    public String getImageUrl() {
        return imageUrl;
    }
}

